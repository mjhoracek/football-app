import { useState } from 'react'
import Head from 'next/head'
import styled from 'styled-components'
import SignUpForm from '../components/Pages/SignIn/SignUpForm'
import LogIn from '../components/Pages/SignIn/LogIn'
import { useRouter } from 'next/router'
import { colors } from '../styles/colors.js'

import { useAuth } from '../contexts/AuthContext'

const Container = styled.div`
    position: relative;
    display: flex;
    flex-direction: row;
    justify-content: center;
    align-items: center;
    width: 100vw;
    height: 100vh;
    overflow: none;
`

const Wrapper = styled.div`
    display: flex;
    position: relative;
    flex-direction: column;
    justify-content: flex-start;
    align-items: flex-start;
    max-width: 400px;
    height: 450px;
    background-color: ${colors.background};
    margin: auto;
    border-radius: 15px;
`

const LeftBox = styled.div`
    position: absolute;
    left: 0;
    display: flex;
    width: 70%;
    height: 100vh;
    background-image: url('/images/football.jpg');
    background-position: center top;
    background-size: cover;
    background-repeat: no-repeat;
`

const RightBox = styled.div`
    position: absolute;
    right: 0;
    display: flex;
    flex-direction: column;
    width: 100%;
    height: 100vh;
    background: rgb(34,34,34);
    background: linear-gradient(90deg, rgba(34,34,34,0.7441351540616247) 0%, rgba(17,17,17,1) 50%, rgba(0,0,0,1) 100%);     
`

const TextBox = styled.div`
    display: flex;
    flex-direction: row;
    justify-content: center;
    align-items: center;
    padding-top: 30px;
    padding-bottom: 15px;
`

const Text = styled.p`
    text-align: center;
    cursor: pointer;
    font-size: 18px;
`

const Message = styled.p`
    font-weight: 500;
    letter-spacing: 1px;
    height: 30px;
    text-align: center;
    padding-top: 10px;
    padding-bottom: 10px;
    padding-bottom: 10px;
    font-size: 14px;
    color: ${colors.purple};
`

export default function Home() {
  const [login, setLogin] = useState(true)
  const [message, setMessage] = useState("Welcome to Mitch's Football App")
  const router = useRouter()
  const { currentUser, error, setError } = useAuth()

  if(currentUser != null){
    router.push('/home')
  }

  return (
    <Container>
        <Head>
          <title>Mitch's Football App</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <LeftBox />

        <RightBox>
          <Wrapper>
            <TextBox>
              <Text 
                style={{ color: login ? 'white' : colors.purple , textDecoration: login ? 'none' : 'underline' }}
                onClick={e => {
                  setLogin(false);
                  setError('');
                }}
              > 
                Sign Up
              </Text>
              <Text
                style={{ color: login ? colors.purple : 'white', textDecoration: login ? 'underline' : 'none' }}
                onClick={e => {
                  setLogin(true);
                  setError('');
                }}
              >
                Log In 
              </Text>
            </TextBox>
            {login ? 
              <LogIn error={error} setError={setError}/> 
              : 
              <SignUpForm error={error} setError={setError} setMessage={setMessage}/> 
            }

            {error.length > 0 ?
                    <Message> {error} </Message> : <Message> {currentUser?.email || "Welcome to Mitch's Football App"} </Message> 
            } 

          </Wrapper>
        </RightBox>

    </Container>
  )
}

